<template>
  <div class="tab-control">
    <div v-for="(item,index) in titles" :key="index" class="tab-control-item" 
      :class="{active: index === currentIndex}" @click="itemClick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  components:{},
  props:{
    //首页传递过来的3个title
    titles: {
      type: Array,
      default () {
        return []
      }
    }
  },
  data(){
    return {
      currentIndex: 0
    }
  },
  watch:{},
  computed:{},
  methods:{
    //监听3个title的点击
    itemClick (index) {
      this.currentIndex = index
      this.$emit('tabClick',index)
    }
  },
  created(){},
  mounted(){}
}
</script>
  
